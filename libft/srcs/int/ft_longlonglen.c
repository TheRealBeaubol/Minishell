#include "libft.h"

int	ft_longlonglen(long long n)
{
	int	cpt;

	cpt = 1;
	if (n == -9223372036854775807 - 1)
		return (20);
	else if (n < 0)
	{
		cpt++;
		n *= -1;
	}
	while (n > 9)
	{
		cpt++;
		n /= 10;
	}
	return (cpt);
}

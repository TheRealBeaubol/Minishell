#include "header.h"

void	get_pwd(t_ms *ms)
{
	int	i;

	i = 0;
	while (ms->env[i] && strncmp(ms->env[i], "PWD=", 4))
		i++;
	ft_dprintf(1, "%s\n", ms->env[i] + 4);
}

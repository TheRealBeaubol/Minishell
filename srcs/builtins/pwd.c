#include "../../includes/header.h"

int	get_pwd(t_ms *ms)
{
	int	i;

	i = 0;
	while (ms->env[i] && strncmp(ms->env[i], "PWD=", 4))
		i++;
	ms->prompt = ft_strjoin(ms->env[i] + 24, ">");
	return (0);
}

#include	"../../includes/header.h"

void	env(t_ms *ms)
{
	int	i;

	i = 0;
	while (ms->env[i])
	{
		write(1, ft_strjoin(ms->env[i], "\n"), ft_strlen(ms->env[i]) + 1);
		i++;
	}
}
